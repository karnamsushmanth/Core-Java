public class Relation {
    public static void main(String a[]) {
        // <, > ,<= ,>= ,==, !=
        // T --> F

        int x = 6;
        int y = 5;
        boolean result = x < y;
        boolean result1 = x > y;
        boolean equal = x == y;
        boolean notequal = x != y;
        System.out.println(result);
        System.out.println(result1);
        System.out.println(equal);
        System.out.println(notequal);
    }

}
